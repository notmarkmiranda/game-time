describe('Ghost', function(){
  context('with default attributes', function(){
    let map = new Map({
      ctx: this.ctx,
      width: realMap[0].length,
      height: realMap.length,
      grid: realMap
    });

		const player = new Player(this.ctx);

    let ghost = new Ghost({
      ctx: this.ctx,
      map: map
    });

    it('should have default attributes', function(){
      assert.isAbove(ghost.x, 0)
      assert.isAbove(ghost.y, 0)
    });

    it('should chase a player', function(){
      let originX = ghost.x
      let originY = ghost.y
      ghost.chasePlayer(10, 10)
      assert.notEqual(ghost.x, originX)
      assert.notEqual(ghost.y, originY)
    });

		it('should be able to calculate the amounts to add to move towards position/player', function(){
			ghost.x = 0;
			ghost.y = 0;
			let distances = ghost.distanceToPlayer(12, 12);
			assert.isAbove(distances[0], 0);
			assert.isAbove(distances[1], 0);
		});

		it('should move by x and y based on distance from player', function(){
			ghost.x = 0;
			ghost.y = 0;
			ghost.chasePlayer(12, 12);
			assert.isAbove(ghost.x, 0);
			assert.isAbove(ghost.y, 0);
		});

		it('should be able to randomly move itself', function(){
			let originX = ghost.x;
			let originY = ghost.y;
			ghost.randomRelocate(map);
			assert.notEqual(ghost.x, originX);
			assert.notEqual(ghost.y, originY);
		});

		it('should be able to confirm a collision', function(){
			player.pillCount = 1
			assert.equal(player.pillCount, 1);
			player.x = 0;
			player.y = 0;
			ghost.x = 0;
			ghost.y = 0;
			assert.notEqual(player.pillCount, 0)
		})

  });
});

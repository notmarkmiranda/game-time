var scale = 12;

Map.prototype.drawWalls = function(){
  for (var y = 0; y < this.height; y++) {
    for (var x = 0; x < this.width; x++) {
      var wall = this.grid[y][x];

      if (wall > 0) {
        this.ctx.fillStyle = "rgb(200,200,200)";
        this.ctx.fillRect(
          x * scale,
          y * scale,
          scale,scale
        );
      }

      if (wall < 0) {
        this.ctx.fillStyle = "rgb(100,100,100)";
        this.ctx.fillRect(
          x * scale,
          y * scale,
          3, 3
        );
      }
    }
  }
}

